"""需求分析 Agent 提示词模板

优化策略：
1. 添加 few-shot 示例，提升输出一致性
2. 结构化输出格式，便于下游 Agent 解析
3. 明确验收标准，确保输出质量
"""

from typing import Optional


# Few-shot 示例（简洁版，用于引导输出格式）
FEW_SHOT_EXAMPLE = """
---
## 示例（参考格式，非实际输出）

### 输入示例
> 开发一个命令行工具，可以批量重命名文件，支持正则表达式匹配

### 输出示例（摘要）

## 1. 项目概述

| 维度 | 说明 |
|------|------|
| **项目类型** | 命令行工具 |
| **核心目标** | 提供高效、灵活的批量文件重命名能力 |
| **目标用户** | 开发者、运维人员、需要批量处理文件的用户 |
| **使用场景** | 本地文件管理、日志归档、照片整理等场景 |

## 2. 核心需求

### 2.1 功能需求

#### FR-001: 正则表达式匹配
- **功能描述**：支持使用正则表达式匹配文件名
- **输入**：正则表达式模式、目标目录
- **输出**：匹配的文件列表
- **验收标准**：能够正确匹配 95% 以上的常见正则模式

#### FR-002: 批量重命名
- **功能描述**：根据规则批量重命名匹配的文件
- **输入**：匹配的文件列表、重命名规则
- **输出**：重命名后的文件
- **验收标准**：支持预览模式，实际执行前显示变更预览

### 2.2 非功能需求

| 类型 | 要求 | 指标 |
|------|------|------|
| **性能** | 处理大量文件 | 1000 个文件 < 5 秒 |
| **可靠性** | 原子操作 | 失败时回滚，不产生中间状态 |
| **易用性** | 清晰的 CLI 接口 | --help 覆盖所有功能 |

---
"""


def build_requirements_prompt(
    user_input: str,
    previous_requirements: Optional[str] = None,
    include_example: bool = True,
) -> str:
    """构建需求分析提示词
    
    Args:
        user_input: 用户输入的需求描述
        previous_requirements: 上一次的需求分析结果（用于迭代优化）
        include_example: 是否包含 few-shot 示例（默认 True）
    
    Returns:
        格式化后的提示词
    """
    previous_section = ""
    if previous_requirements:
        previous_section = f"""

## 📝 历史需求分析（请在此基础上优化）

{previous_requirements}

---
"""

    example_section = FEW_SHOT_EXAMPLE if include_example else ""
    
    return f"""# 角色定义

你是一位资深产品经理和需求分析师，擅长将模糊的需求转化为清晰、可执行的技术规格。

# 核心任务

深入分析用户需求，输出结构化、详尽且可执行的需求分析报告。
{example_section}
---

# 🎯 用户需求

{user_input}
{previous_section}
---

# 📋 输出规范

请使用以下 Markdown 格式输出，内容精炼，聚焦核心需求。

## 1. 项目概述

| 维度 | 说明 |
|------|------|
| **项目类型** | [应用程序/工具/算法/库/数据分析/其他] |
| **核心目标** | [一句话概括要解决的核心问题] |
| **目标用户** | [明确的用户画像] |
| **使用场景** | [何时何地如何使用] |

## 2. 核心需求

### 2.1 功能需求

列出核心功能（FR-001, FR-002...），控制在 5-7 项。每项包含：

#### FR-001: [功能名称]
- **功能描述**：清晰的功能说明
- **输入**：所需的输入数据
- **输出**：预期的输出结果
- **验收标准**：如何判定功能完成

#### FR-002: [功能名称]
...（按此格式继续）

### 2.2 非功能需求

根据项目类型列出 3-5 项关键非功能需求：

| 类型 | 要求 | 指标 |
|------|------|------|
| **性能** | 响应时间/吞吐量/处理能力 | [具体数值] |
| **可靠性** | 准确性/稳定性/容错能力 | [具体要求] |
| **易用性** | 易学性/文档/错误处理 | [具体要求] |
| **可扩展性** | 增长能力/扩展能力 | [具体要求] |
| **约束条件** | 平台/技术/资源限制 | [具体限制] |

## 3. 问题空间分析

### 3.1 核心问题
- **问题定义**：需要解决的根本问题
- **深层原因**：问题产生的根本原因或痛点
- **现有方案局限**：（如适用）现有解决方案的不足

### 3.2 约束条件
- **技术约束**：必须/不能使用的技术
- **环境约束**：运行/部署环境要求
- **资源约束**：时间/人力/预算限制

## 4. 关键实体与流程

### 4.1 核心实体/概念

列出项目中 3-5 个关键实体或概念：

1. **[实体名]**：[简要描述]
2. **[实体名]**：[简要描述]
3. **[实体名]**：[简要描述]

### 4.2 主要流程

描述核心处理流程或交互流程：

```
[输入] → [处理步骤1] → [处理步骤2] → [输出]
```

### 4.3 输入输出规格
- **输入**：数据来源、格式、规模
- **输出**：结果格式、用途

## 5. 待澄清问题

- [ ] [需要进一步明确的要点]
- [ ] [不确定的业务规则]

---

# ✅ 输出质量检查

输出前请确认：
- [ ] 准确识别项目类型
- [ ] 列出所有显式需求（FR-001 格式）
- [ ] 功能需求有明确验收标准
- [ ] 非功能需求与项目类型匹配
- [ ] 识别核心实体和流程
- [ ] 约束条件清晰明确
- [ ] 内容精炼无冗余

**请开始分析：**
""".strip()
