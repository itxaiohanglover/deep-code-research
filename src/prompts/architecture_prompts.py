"""架构设计 Agent 提示词模板"""

from typing import Optional

from src.utils.context_manager import fit_artifacts_for_prompt


def build_architecture_prompt(
    user_input: str,
    requirements: Optional[str] = None,
    tech_research: Optional[str] = None
) -> str:
    """构建架构设计提示词
    
    Args:
        user_input: 用户输入
        requirements: 需求分析结果
        tech_research: 技术研究结果
    
    Returns:
        格式化后的提示词
    """
    if not requirements:
        return user_input
    
    # 使用 ContextManager 智能分配空间（总共 6000 字符，需求优先）
    artifacts = {
        "requirements": requirements or "",
        "tech_research": tech_research or "",
    }
    
    fitted = fit_artifacts_for_prompt(
        artifacts,
        max_total_chars=6000,
        priorities=["requirements", "tech_research"]
    )
    
    req_preview = fitted.get("requirements", "")
    tech_preview = fitted.get("tech_research", "")
    
    tech_section = f"""
## 技术研究
{tech_preview}""" if tech_preview else ""
    
